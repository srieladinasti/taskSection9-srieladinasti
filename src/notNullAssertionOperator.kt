fun main() {
    var angka1: Int? = 0
    println(angka1!! + 3)

    var angka2: Int? = 3
    println(angka2!! + 5)

    var angka3: Int? = 5
    println(angka3!! + 15)

    var angka4: Int? = 15
    println(angka4!! - 5)

    var angka5: Int? = 5
    println(angka5!! - 3)

    var angka6: Int? = 3
    println(angka6!! - 0)

    var angka7: Int? = 0
    println(angka7!! * 3)

    var angka8: Int? = 3
    println(angka8!! * 5)

    var angka9: Int? = 5
    println(angka9!! * 15)

    var angka10: Int? = 15
    println(angka10!! / 5)
}